#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * op_add - add numbers
 *
 * @a: input a
 * @b: input b
 *
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract numbers
 *
 * @a: input a
 * @b: input b
 *
 * Return: 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mult numbers
 *
 * @a: input a
 * @b: input b
 *
 * Return: 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide numbers
 *
 * @a: input a
 * @b: input b
 *
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod of two numbers
 *
 * @a: input a
 * @b: input b
 *
 * Return: 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
